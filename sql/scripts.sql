    SELECT SALARY
    FROM Worker
    GROUP BY SALARY
    HAVING COUNT(WORKER_ID) > 1
)

SELECT w1.WORKER_ID, w1.FIRST_NAME, w1.LAST_NAME, w1.SALARY
FROM Worker w1
JOIN Worker w2 ON w1.SALARY = w2.SALARY AND w1.WORKER_ID <> w2.WORKER_ID
ORDER BY w1.SALARY

--Q12
