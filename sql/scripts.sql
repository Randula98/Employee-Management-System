--Q13
WITH EmployeeRank AS (
    SELECT 
        FIRST_NAME, 
        LAST_NAME, 
        DEPARTMENT, 
        SALARY,
        ROW_NUMBER() OVER (PARTITION BY DEPARTMENT ORDER BY SALARY DESC) AS SalaryRank
    FROM Worker
)
SELECT FIRST_NAME, LAST_NAME, DEPARTMENT, SALARY
FROM EmployeeRank
WHERE SalaryRank = 1

